package com.cifpceuta.gridview_compisclase;

public class Usuario {
    int imagen;
    String nombre;

    public Usuario() {
    }

    public Usuario(int imagen, String nombre) {
        this.imagen = imagen;
        this.nombre = nombre;
    }

    public int getImagen() {
        return imagen;
    }

    public void setImagen(int imagen) {
        this.imagen = imagen;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
